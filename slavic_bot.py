def build_message(holiday: tuple, days_left: int) -> str:
    """Формирует красивое сообщение для Telegram."""
    month, day, name, emoji, description, rituals = holiday

    if days_left == 0:
        header = f"🎉 *Сегодня — {name}* {emoji}"
        timing = "Праздник наступил!"
    elif days_left == 1:
        header = f"⏰ *Завтра — {name}* {emoji}"
        timing = "Остался 1 день"
    else:
        header = f"📅 *Через {days_left} дней — {name}* {emoji}"
        timing = f"Осталось {days_left} дней"

    ritual_lines = "\n".join(f"  • {r}" for r in rituals)

    return (
        f"{header}\n"
        f"_{timing}_\n\n"
        f"{description}\n\n"
        f"*Традиции и обряды:*\n{ritual_lines}"
    )


def get_holidays_for_date(target_date: date) -> list:
    """Возвращает праздники, до которых осталось REMIND_DAYS или 0 дней."""
    results = []
    today = date.today()

    for holiday in HOLIDAYS:
        month, day = holiday[0], holiday[1]

        try:
            holiday_date = date(today.year, month, day)
        except ValueError:
            continue

        if holiday_date < today:
            try:
                holiday_date = date(today.year + 1, month, day)
            except ValueError:
                continue

        days_left = (holiday_date - today).days

        if days_left in REMIND_DAYS or days_left == 0:
            results.append((holiday, days_left))

    return results


async def send_reminders(bot: Bot):
    """Основная функция: проверяет праздники и отправляет сообщения."""
    today = date.today()
    logging.info(f"Проверка праздников на {today}")

    triggered = get_holidays_for_date(today)

    if not triggered:
        logging.info("Нет праздников для напоминания сегодня")
        return

    for holiday, days_left in triggered:
        msg = build_message(holiday, days_left)
        await bot.send_message(
            chat_id=CHAT_ID,
            text=msg,
            parse_mode="Markdown"
        )
        logging.info(f"Отправлено: {holiday[2]} (через {days_left} дней)")


async def health_handler(request):
    """Health check endpoint для Render."""
    return web.Response(text="OK")


async def main():
    bot = Bot(token=BOT_TOKEN)

    me = await bot.get_me()
    logging.info(f"Бот запущен: @{me.username}")

    await send_reminders(bot)

    scheduler = AsyncIOScheduler(timezone="Europe/Moscow")
    scheduler.add_job(
        send_reminders,
        trigger="cron",
        hour=CHECK_HOUR,
        minute=CHECK_MINUTE,
        args=[bot]
    )
    scheduler.start()
    logging.info(f"Планировщик запущен: проверка каждый день в {CHECK_HOUR:02d}:{CHECK_MINUTE:02d}")

    # HTTP-сервер для Render (web service требует открытый порт)
    port = int(os.environ.get("PORT", 8080))
    app = web.Application()
    app.router.add_get("/", health_handler)
    runner = web.AppRunner(app)
    await runner.setup()
    site = web.TCPSite(runner, "0.0.0.0", port)
    await site.start()
    logging.info(f"HTTP-сервер запущен на порту {port}")

    try:
        await asyncio.Event().wait()
    except (KeyboardInterrupt, SystemExit):
        logging.info("Бот остановлен")
        scheduler.shutdown()
        await runner.cleanup()


if __name__ == "__main__":
    asyncio.run(main())
